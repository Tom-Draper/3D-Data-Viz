function gen() {
  let array = [];
  for (let i = 0; i < 5; i++) {
    array.push([]);
    for (let j = 0; j < 2; j++) {
      array[i].push([]);
      for (let k = 0; k < 4; k++) {
        array[i][j].push(Math.floor(Math.random() * 300) * i - 5*k);
      }
    }
  }
  return array;
}

